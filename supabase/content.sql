<h3>Почему нужен был Христос?</h3>
<p>Усилия человека бесполезны — они дают лишь временное утешение, но не решают коренную проблему. Человек всё равно остаётся отделённым от Бога, грешником и под властью сатаны. Духовную проблему невозможно решить человеческими методами. Психологи, коучи, философии — всё это работает на уровне следствий, не касаясь причины.</p>
<p>Поэтому Бог приходит к человеку Сам. Мост между Богом и человеком восстановил Сам Господь:</p>
<p><em>«Бог доказал нам Свою любовь тем, что Христос умер за нас, пока мы были ещё грешниками»</em> (Рим 5:8). Это полностью Его инициатива — Он не ждал, пока люди сами придут, раскаются и подготовятся.</p>

<h3>Кто такой Иисус Христос?</h3>
<p><strong>Иисус</strong> — означает «Спаситель». Он был рождён на землю с одной целью. Это был план Бога, составленный до создания мира. <strong>Христос</strong> — это не фамилия. Это функция. «Христос» означает «Помазанник» — тот, кому Бог дал Свою силу для исполнения Его воли. Есть дела, которые без Божьей силы невозможно совершить, как ни старайся.</p>

<h3>Первая функция: Пророк</h3>
<p>Функция пророка — принести людям Божий ответ, донести путь и дать выход. Христос выполнил эту функцию, придя и показав путь к Богу:</p>
<p><em>«Я путь, истина и жизнь. Никто не приходит к Отцу, как только через Меня»</em> (Ин 14:6).</p>
<p>Уйдя от Бога, человеку нужно было вернуться. Иисус принёс ответ и, указав на Себя, сказал: вот куда идите. Таким образом Он решил проблему ухода от Бога.</p>

<h3>Вторая функция: Священник</h3>
<p>Ветхозаветные священники имели власть от Бога отпускать грехи через жертву — кровь чистых животных. Но это нужно было повторять каждый год. Новые грехи — новые жертвы.</p>
<p>Христос пришёл как истинный Первосвященник и выступил в роли Агнца. Будучи Богом, имея власть на небе и на земле — Он позволил распять Себя. И таким образом <strong>искупил нас навсегда</strong>. Он совершил совершенную Жертву, которую не нужно повторять. Он искупил грехи прошлого, настоящего и будущего.</p>
<p><em>«Закон духа жизни во Христе Иисусе освободил меня от закона греха и смерти»</em> (Рим 8:2).</p>
<p>Теперь посредник между Богом и человеком не нужен. Ты можешь сам исповедовать перед Богом.</p>


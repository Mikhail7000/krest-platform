<h2>7 Благословений, которые мы получаем во Христе</h2>

<h3>1. Изменение статуса — ты становишься чадом Божьим</h3>
<p><em>«А тем, которые приняли Его, верующим во имя Его, дал власть быть чадами Божиими»</em> (Иоанна 1:12).</p>
<p>Ты больше не просто творение Бога — ты теперь Его сын или дочь. Это не результат усилий, не заслуга, а подарок через веру. Как ребёнок президента не зарабатывает свой статус — он просто рождается в этой семье. Так и ты: рождаясь свыше, становишься частью Божьей семьи.</p>
<ul>
  <li>Бог называет тебя Своим.</li>
  <li>Ты больше не сирота.</li>
  <li>Ты имеешь доступ к Отцу.</li>
</ul>

<h3>2. Бог поселяется в тебе — Святой Дух</h3>
<p><em>«Разве не знаете, что вы храм Божий, и Дух Божий живёт в вас?»</em> (1 Коринфянам 3:16).</p>
<p>Бог больше не просто где-то в небе. Он внутри тебя. Он не приходит и уходит — Он поселился. Его Дух — это Личность, которая с тобой 24/7.</p>
<p>Что делает Святой Дух: учит и открывает истину, обличает и направляет, даёт силу против греха, ведёт в ежедневной жизни. Это и есть суть спасения: Бог снова с человеком — как в Эдеме.</p>

<h3>3. Ответы на молитвы</h3>
<p><em>«Если чего попросите во имя Моё — Я то сделаю»</em> (Иоанна 14:14).</p>
<p>Ты не просто можешь говорить с Богом — ты можешь ожидать, что Он услышит и ответит. Это право ребёнка, не выпрошенная милостыня. Молитва — это сотрудничество с Богом: ты исполняешь Его волю — и Он отвечает.</p>

<h3>4. Служение ангелов</h3>
<p><em>«Не все ли они суть служебные духи, посылаемые на служение для тех, кто наследует спасение?»</em> (Евреям 1:14).</p>
<p>Ты не один. С тобой — ангелы, реальные духовные существа, которые защищают, приносят ответы, исполняют поручения Бога, устраивают обстоятельства. Когда ты молишься — в духовном мире происходит действие. Это твой духовный bodyguard.</p>

<h3>5. Власть над силами тьмы</h3>
<p><em>«Се, даю вам власть наступать на змей и скорпионов и на всю силу вражью»</em> (Луки 10:19).</p>
<p>Ты больше не жертва духовных атак — ты имеешь власть. Власть изгонять бесов, разрушать ложь, молиться с результатом, освобождать других. Применять власть — это не просить, а приказывать, как делает руководитель, а не проситель.</p>

<h3>6. Жительство на небесах</h3>
<p><em>«Наше жительство — на небесах»</em> (Филиппийцам 3:20).</p>
<p>Твоё гражданство изменено. Ты уже принадлежишь Царству Небесному. Здесь ты временный житель, а твой дом — у Отца. Ты не боишься смерти, живёшь с вечной перспективой и смотришь глазами неба.</p>

<h3>7. Божья цель — смысл жизни</h3>
<p><em>«Идите, научите все народы…»</em> (Матфея 28:18–20).</p>
<p>Бог дал тебе цель, которую невозможно разрушить. Это не деньги, не семья, не успех — а исполнение Его воли: ставить учеников, утверждать Церковь, распространять Царство. Когда цель от Бога — её нельзя забрать. Даже в тюрьме, даже в бедности ты продолжаешь быть полезным для Царства.</p>

<h2>Вывод</h2>
<p>Каждое благословение — это не заслуга, а наследие. Ты не просто спасён — ты <strong>принят, поселён, вооружён, обеспечен и направлен</strong>.</p>
<ul>
  <li>Бог дал тебе всё, чтобы исполнить Его волю.</li>
  <li>Вся власть принадлежит Христу.</li>
  <li>Если ты сосредоточен на Божьей миссии — Бог будет открывать двери и прилагать людей.</li>
</ul>
<p><strong>Самое великое благословение — знать, кто ты, с кем ты, и зачем ты живёшь.</strong></p>

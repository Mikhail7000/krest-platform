<p><em>«Для того и пришёл Сын Божий, чтобы разрушить дела дьявола»</em> (1 Ин 3:8).</p>
<p>Христос — единственный, кто имел власть над сатаной. Чтобы уволить начальника — должен прийти его начальник. Только Бог имеет власть над сатаной.</p>


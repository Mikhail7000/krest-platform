<h2>Как Бог решил духовную проблему человека</h2>
<p>Бог решил духовную проблему духовным способом — послав Спасителя, Иисуса Христа, на землю. Христос разрушил дела дьявола, даровал прощение грехов, пролив Свою кровь, и вернул человека к Богу.</p>

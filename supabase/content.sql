<h3>Что изменилось после грехопадения?</h3>
<p>Человек стал жить в шести состояниях. В первую очередь изменился его статус.</p>

<h3>1) Чадо Дьявола — Еф 2:1</h3>
<p>«И вас, мёртвых по преступлениям и грехам вашим...» Чадо Дьявола — это не сатанисты и не те, кто осознанно поклоняется злу. Это <strong>духовно мёртвый человек</strong>, находящийся под властью сатаны, под властью духа лжи.</p>
<p>Человек не может ни во что не верить — мы созданы как духовные существа. Когда ты не веришь Богу — ты автоматически веришь в альтернативу. Если ты не веришь, что Иисус Христос решил на кресте абсолютно всё — ты автоматически веришь во что-то другое. Власть сатаны происходит через культуру мира: человек воспитывается ею, в нём формируются ложные желания и ложные цели.</p>

<h3>2) Идолопоклонство — Еф 2:2</h3>
<p>«По обычаю мира сего, по воле князя, господствующего в воздухе...» Культурой мира управляет дьявол, задавая тренды и повестку. Всё, что нам говорят о счастье и смысле жизни — это ложные критерии, не от Бога.</p>
<p>Суть идолопоклонства — это не только поклонение другим богам. На повседневном уровне это то, <strong>что занимает место Бога в твоей жизни</strong>. То, ради чего ты живёшь, чему служишь, чему предан. Если это семья — семья твой идол. Если деньги и карьера — они твой идол. Бог дал всё это как благословение, но когда ты ставишь это выше Бога — это возводится в категорию идола. Идол высасывает энергию, но не восполняет её.</p>

<h3>3) Психические проблемы — Мф 11:28</h3>
<p>На пути к идолам мы начинаем переживать. Достигли цели — боимся потерять. Получили временное удовлетворение — ищем новые цели. Даже богатые и известные люди, достигшие всего материального, теряют смысл жизни — там счастья нет.</p>
<p>«Придите ко Мне все труждающиеся и обременённые, и Я успокою вас» — Иисус поставил всем диагноз. Люди живут в беспокойстве и обременении, пытаясь достичь счастья своими силами, и у них это не получается.</p>

<h3>4) Физические проблемы — Дн 8:7</h3>
<p>Духовные проблемы проявляются в теле: болезни из-за постоянных стрессов, конфликты с людьми, бессонница, криминал, зависимости. За многими болезнями и одержимостями стояли духи — когда люди освобождались на духовном уровне, приходило исцеление.</p>
<p><strong>Цепочка:</strong> покровительство сатаны (Еф 2:1) → дух под его властью (Еф 2:2) → переживания души (Мф 11:28) → разрушение тела (Дн 8:7).</p>

<h3>5) Смерть — Суд — Ад — Евр 9:27</h3>
<p>«И как человекам положено однажды умереть, а потом суд». Это судьба каждого грешника. Не по принципу хороший человек или плохой — в ад идёт тот, кто является чадом дьявола. Он просто идёт домой к своему отцу. Как родители забирают ребёнка из садика — так и после земной жизни грешник попадает к тому, чьим чадом является.</p>

<h3>6) Поколения — Исх 20:4-5</h3>
<p>«Наказывающий детей за вину отцов до третьего и четвёртого рода...» Существует духовный принцип передачи: как внешность и склонности к болезням передаются физически, так и духовное наследие передаётся из рода в род. Грешник рождает грешника. Передаётся мышление, страхи, модели поведения.</p>
<p>Ребёнок видит, кому служат его родители — деньгам, страху, мнению людей — и перенимает эту модель. За этим стоит духовная составляющая. <strong>Единственный выход из проклятия поколений — прийти ко Христу.</strong></p>

<h3>Итог</h3>
<p>Шесть состояний показывают причинно-следственную связь. Человек ушёл от Бога, попал под власть лжи, его цели стали ложными — из-за этого постоянные переживания, разрушение жизни на душевном и физическом уровне, проблема загробной жизни и передача этого наследия детям. В каждом состоянии человек без Бога должен узнать себя.</p>

<h3>Начало — связь Бога и человека</h3>
<p>В начале Бог сотворил человека, и между Богом и человеком была неразрывная связь. Для человека было нормально просто существовать с Богом. Быть без Бога — это болезнь или смерть. Это было естественное состояние для человека.</p>

<h3>Бог — источник жизни</h3>
<p>Бог является источником жизни и источником всего сущего. Его имя <strong>«Сущий»</strong> — значит существующий. Он не может не существовать. Бог, находящийся вне рамок мира материи и времени, сотворил материального человека и материальный мир.</p>
<p><strong>Бог есть Слово.</strong> Всё построено на слове Бога. И всё подчиняется слову Бога — кроме человека. Только человек противится слову Бога.</p>
<p>Бог вложил в каждое творение <strong>принцип счастья</strong>. Счастье для любого творения — это его естественная среда обитания. Рыбе Бог дал воду, дереву — землю, птице — небо. А для человека естественная среда обитания — это Бог, потому что человек является духовным существом.</p>

<h3>1) Божий Образ — Быт 1:27</h3>
<p>Что означает «по образу Божьему»? Не Бог по образу человека, а человек по образу Бога. Нужно понимать, кто такой Бог. <strong>Бог — это Слово</strong> (Ин 1:1): Слово = Дух = Жизнь. Он также Дух.</p>
<p>Поэтому человек из себя представляет набор слов, идеологию, в которую верит, — это его мышление. Божий образ в человеке — это Божий образ мышления, начинка человека.</p>

<h3>2) Духовная личность — Быт 2:7</h3>
<p>В начале человек был пустышкой — даже не живым существом. Бог вдохнул в него Свой Дух. Дыхание — значит Дух Божий. Внутри человека жил Бог, и человек знал Божье слово на уровне сущности — ему не нужна была Библия, ему не надо было ничему учиться. Каждый человек мыслит исходя из слов, которые в него заложены.</p>

<h3>3) Благословение — Быт 1:28</h3>
<p>Бог благословил человека и сказал: <em>«Наполняйте, владычествуйте, наполняйте Землю».</em> Помимо жизни с Богом, в материальном мире Бог дал <strong>5 видов благословения:</strong></p>
<ul>
  <li>Семья</li>
  <li>Здоровье</li>
  <li>Финансы</li>
  <li>Карьера</li>
  <li>Власть</li>
</ul>
<p>Человек развивал себя, Землю, свою зону ответственности. Самое высшее благословение — <strong>миссия</strong>: будучи носителем Божьей идеологии, распространять её по земле.</p>

<h3>4) Завет — Быт 2:16-17</h3>
<p>Бог дал распоряжение: <em>«От любого дерева в саду ешь, но от дерева познания добра и зла не ешь — ибо в день, в который ты вкусишь от него, умрёшь».</em></p>
<p>Этот завет связан с жизнью. Когда связь Бога и человека не нарушена — человек имеет жизнь вечную: жить в счастье и наслаждении с Богом. Это цель творения — чтобы человек был вместе и счастлив с Богом.</p>

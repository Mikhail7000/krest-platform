<h3>Что значит быть с Богом?</h3>
<p>Когда человек с Богом — это значит учиться от Бога и пребывать в Его слове. Когда твоя программа записана из Божьего образа мышления — у тебя есть всё, чтобы жить счастливой жизнью, выстроить все сферы так, как Бог задумал. В этом есть все ответы — в этом и есть абсолютное счастье.</p>
<p>Как можно разрушить связь с тем, кто всемогущий, вездесущий? Только одним способом — уведя человека от Слова Божьего, введя его в сомнение.</p>

<h3>1) Уход — Быт 3:1-6</h3>
<p>Человек разорвал отношения с Богом. Каким образом? Он услышал другие слова. Мы все — продукт слов. Всё, что мы защищаем, наши точки зрения и мнения — это только то, что было в нас посеяно и укоренено.</p>
<p>Пришла ложь. Если Бог — это слово истины, то в человека вошло ложное слово. У лжи только одна цель — разрушать. Сатана использовал искусство обольщения, состоящее из трёх шагов:</p>
<ul>
  <li><strong>Сомнение:</strong> «Подлинно ли сказал Бог?» — Нет смысла говорить что-то другое тому, кто уверен на 100%. Сатана работает, чтобы усомнить. В голову приходит мысль, не соответствующая Писанию, какая-то философия, которая начинает развиваться.</li>
  <li><strong>Альтернатива:</strong> «Нет, не умрёте» — если не так, то как? Если не Библия, то что? Если не Иисус, то как спастись? Приходит мысль, что можно по-другому, не так, как сказал Бог.</li>
  <li><strong>Желание:</strong> «Увидела жена, что дерево вожделенно» — рождается желание войти в альтернативу. Желание возникает через слово, которое попало внутрь. Над чем размышляешь — к тому и придёшь.</li>
</ul>
<p>Суть коренной проблемы: попала ложь, люди приняли её и начали размышлять. Размышления приводят к грехопадению.</p>

<h3>2) Грехопадение — Рим 3:23</h3>
<p>«Все согрешили и лишены славы Божией». Что такое грех? Грех — это неверие, то есть сомнение в Божьем Слове и отказ подчиниться ему. Когда слово сатаны вошло в человека, а слово Божье вышло — произошла <strong>духовная смерть</strong>.</p>
<p><strong>Кто такой грешник?</strong> Это человек, отделённый от Бога, который не может или не хочет верить и подчиняться Божьему Слову. Неверие в Бога — это автоматически вера в ложь. А кому веришь — тот управляет твоей жизнью.</p>

<h3>3) Власть сатаны — Ин 8:44</h3>
<p>Дьявол взял власть над человеком через то, что человек согласился с ложью. За кем следуешь в учении, кому доверяешь своё мировоззрение — тому вверяешь и управление своей жизнью.</p>
<p>Власть сатаны — это программа, которая фокусирует на ложной цели и даёт ложные мечты. Дьявол управляет — нам говорят, чего хотеть. Когда человек был с Богом, он хотел делать то, что говорит Бог. Начал слушать другое — начал делать другое.</p>
<p>Откуда все страдания, переживания, болезни? Человек попал во власть судьбы. А судьба — это результат желаний, которые возникли через ложь.</p>

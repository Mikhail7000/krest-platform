## Цель и суть

Во время эпохи пятницы мы выходим на места действия в нашем городе, единодушно с командой. Главная цель — найти ученика, который закрепится в горнице, за счёт лёгкого и понятного донесения Евангелия. С собой можно и нужно брать уже действующих учеников, чтобы они практиковались вместе с лидерами: слушали и видели, как можно проповедовать, и сами начинали действовать.

⭐️ **Мф. 28:19–20** — ключевое

## 1. Духовная подготовка

Перед выходом важно подготовиться духовно — единодушная молитва команды:

- за божественные встречи
- за открытые сердца людей
- за водительство Духа Святого
- за смелость и дерзновение проповедовать людям

⭐️ **Деян. 1:14** — единодушно в молитве

## 2. Определение цели выхода

Перед выходом команда понимает:

- зачем мы идём
- какая наша миссия
- какой результат мы ожидаем

🎯 Цель: найти «своих» людей, готовых услышать Евангелие.

## 3. Выбор места

Нужно выбирать места, где есть поток людей и атмосфера общения.

✅ Подходящие места:

- бары, рестораны, кафе
- молодёжные места
- общественные пространства
- чайные церемонии
- настольные игры / мафия

Важно выбирать места, где люди открыты к разговору. Пробуйте разные места — необязательно останавливаться на одном. Можно заранее составить список групп и мероприятий, чтобы подготовиться к эпохе пятницы.

## 4. Состав команды

Оптимальный формат: 2–4 человека.

- легче общаться
- легче поддерживать разговор
- есть духовная поддержка (Иисус всегда отправлял по двое)

Пример ролей:

- один начинает разговор
- второй поддерживает диалог
- третий молится внутри — важно одерживать победу в первую очередь в духе

## 5. Алгоритм действий

**Шаг 1. Наблюдение.** Сначала команда наблюдает: кто открыт, кто расположен к общению, где есть возможность начать разговор.

**Шаг 2. Начало разговора.** Начинаем простым человеческим общением, выстраиваем душевные связи, а далее так или иначе вшиваем Евангелие.

**Шаг 3. Переход к духовному.** После установления контакта разговор мягко переводится в духовную тему. Можно спросить:

- веришь ли ты в Бога?
- задумывался ли о смысле жизни?
- что для тебя духовность?
- на чём строится твоя жизнь, за что ты держишься, когда что-то происходит?

⚠️ Важно не давить, а вести диалог. Слушаем, в чём человек находится, о чём говорит и размышляет, пытаемся понять, какая «книга» говорит через него. Дальше можно аккуратно зайти через идол человека с крестом и начинать проповедовать.

**Шаг 4. Свидетельство.** После разговора можно поделиться:

- своей историей
- тем, как Бог изменил жизнь
- простым Евангелием, своими словами

⚠️ Главная идея: Иисус Христос пришёл решить духовную проблему человека.

**Шаг 5. Приглашение.** Если человек открыт, его можно пригласить:

- на горницу
- на встречу (рассказать крест)

⚠️ По итогу нужно обменяться контактами. Можно записать видео-кружок с человеком — чтобы, когда он зайдёт в чат, уже видел нас вместе.

## 6. Важные принципы

1. **Не спорить.** Цель — не выиграть спор и не разрушить платформу, на которой стоит человек, а посеять семя Евангелия. Дальше с ним будет работать Бог.
2. **Уважение.** Искренность, внимательность в разговоре и уважение — так завоёвывают людей для Бога.
3. **Простота.** Не нужно говорить сложно. Евангелие должно звучать просто и понятно.
4. **Различение.** Понимать, готов ли человек слушать. Если не готов — не давить.


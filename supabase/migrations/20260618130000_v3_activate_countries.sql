-- Активировать все 9 стран
UPDATE public.countries
   SET status = 'active'
 WHERE status <> 'active';

-- Скрыть города, которых нет в списке Михаила (остаются в БД, но не в онбординге)
UPDATE public.cities
   SET status = 'coming_soon'
 WHERE country_id = 1
   AND name_ru IN ('Владимир', 'Находка');
